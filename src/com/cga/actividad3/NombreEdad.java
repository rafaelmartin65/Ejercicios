/**
 * @author Rafael Martin
 * 
 * Actividad número #3 -> ejercicio 1: Pograma que muestra por pantalla el nombre, la edad y el nombre del curso.
 * 
 */

public class NombreEdad {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		// define variables
		String nombre, nombreCurso;
		int edad;
		
		// Asignamos valores
		nombre = "Rafael";
		nombreCurso = "Programación Java";
		edad = 18;
		
		// Salida por pantalla
		System.out.println("Mi nombre es: " + nombre);
		System.out.println("Mi edad es: " + edad);
		System.out.println("El nombre del curso es: " + nombreCurso);

	}

}
